def choose_colors(colors_dict) -> tuple:
    """Returns a two-tuple of two-tuples of the form (((r,g,b) shape_color_label), ((r,g,b),letter_color_label))"""
    color_label = random.choice(list(colors_dict.keys()))
    rgb_value = random.choice(colors_dict[color_label])
    shape_color = (rgb_value, COLOR_TO_LABEL_DICT[color_label])

    second_color_label = random.choice(
        [key for key in colors_dict.keys() if key != color_label]
    )
    second_rgb_value = random.choice(colors_dict[second_color_label])
    letter_color = (second_rgb_value, COLOR_TO_LABEL_DICT[second_color_label])

    return shape_color, letter_color


